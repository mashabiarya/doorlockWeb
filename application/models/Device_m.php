<?php
defined('BASEPATH') or exit('No direct script access allowed');

class Device_m extends CI_Model
{
    public function get($id = null)
    {
        $this->db->select('*');
        $this->db->from('device');
        if ($id != null) {
            $this->db->where('id', $id);
        }
        $this->db->order_by('id', 'asc');
        $query = $this->db->get();
        return $query;
    }
}
